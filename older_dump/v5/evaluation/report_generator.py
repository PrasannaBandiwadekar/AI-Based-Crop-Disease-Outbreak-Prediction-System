import os
import sys
import pandas as pd

# Add src and evaluation to path
sys.path.append(os.path.join(os.path.dirname(__file__), ".."))
from evaluation import metrics, seasonal_analysis, lead_time_analysis, stress_test
from src import config

def generate_final_report():
    print("Generating V5 Final Validation Report...")
    
    # 1. Gather Data
    m = metrics.compute_metrics()
    s = seasonal_analysis.run_seasonal_analysis()
    l = lead_time_analysis.run_lead_time_analysis()
    st = stress_test.run_stress_test()
    
    # 2. Determine Verdict
    verdict = "NO GO"
    if m["event_level_recall"] >= 0.90:
        valid_lead_pct = (l["Status"] == "Valid (3-7d)").mean()
        if valid_lead_pct >= 0.70:
            verdict = "GO"
        else:
            verdict = "CONDITIONAL"
            
    # 3. Build Markdown
    l_md = "| " + " | ".join(l.columns) + " |\n"
    l_md += "| " + " | ".join(["---"] * len(l.columns)) + " |\n"
    for _, row in l.iterrows():
        l_md += "| " + " | ".join(map(str, row.values)) + " |\n"

    report = f"""# V5 Final Validation Report: Red Rot Early Warning System

## 1. Executive Summary
- **Final Verdict:** {verdict}
- **Event-Level Recall:** {m['event_level_recall']:.2%}
- **False Alert Rate:** {m['false_alert_rate']:.2%}

## 2. Event-Level Performance
| Metric | Value |
|--------|-------|
| Total Historical Outbreaks | {m['total_events']} |
| Successfully Detected | {m['detected_events']} |
| Missed Events | {m['total_events'] - m['detected_events']} |

## 3. Lead Time Distribution
{l_md}

## 4. Seasonal Stability
| Season | Alert Density (%) |
|--------|-------------------|
| Pre-Monsoon | {s.get('Pre-Monsoon', {}).get('alert_density (%)', 0):.2f}% |
| Monsoon | {s.get('Monsoon', {}).get('alert_density (%)', 0):.2f}% |
| Post-Monsoon | {s.get('Post-Monsoon', {}).get('alert_density (%)', 0):.2f}% |

## 5. Stress Test Results
- **Rain Imputation:** {"PASS" if st['rain_imputation_success'] else "FAIL"}
- **Spike Suppression (2-of-3 Rule):** {"PASS" if st['smoothing_rule_spike_suppressed'] else "FAIL"}

---
*Generated by V5 Validation Framework*
"""
    
    report_path = os.path.join(config.BASE_DIR, "evaluation", "V5_FINAL_VALIDATION_REPORT.md")
    with open(report_path, "w", encoding='utf-8') as f:
        f.write(report)
        
    print(f"Final Report Saved to {report_path}")
    print(f"FINAL VERDICT: {verdict}")

if __name__ == "__main__":
    generate_final_report()
